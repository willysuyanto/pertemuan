<?php 

//while
$i = 0;
while ($i <= 10) {
    echo "perulangan dengan while ke $i";
    echo "<br>";
    $i++;
}

// do while
$i = 11;
do {
    echo "perulangan dengan do while";
    echo "<br>";
    $i++;
} while ($i <= 10);

//for
for ($i=0; $i <=10; $i++) { 
    echo "perulangan for ke $i";
    echo "<br>";
}



?>